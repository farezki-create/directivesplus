
import { useState, useEffect, useRef } from "react";
import { supabase } from "@/integrations/supabase/client";
import { Document } from "@/types/documents";

export const useDocumentLoader = (documentId: string | null) => {
  const [document, setDocument] = useState<Document | null>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const [retryCount, setRetryCount] = useState(0);
  const abortControllerRef = useRef<AbortController | null>(null);
  const loadingRef = useRef(false);

  useEffect(() => {
    const loadDocument = async () => {
      if (!documentId) {
        console.log("useDocumentLoader: Aucun documentId fourni");
        setLoading(false);
        return;
      }

      // √âviter les chargements multiples simultan√©s
      if (loadingRef.current) {
        console.log("useDocumentLoader: Chargement d√©j√† en cours, annulation");
        return;
      }

      // Annuler la requ√™te pr√©c√©dente si elle existe
      if (abortControllerRef.current) {
        abortControllerRef.current.abort();
      }

      // Cr√©er un nouveau controller pour cette requ√™te
      const abortController = new AbortController();
      abortControllerRef.current = abortController;
      loadingRef.current = true;
      
      try {
        setLoading(true);
        setError(null);
        
        console.log(`AUDIT: ===========================================`);
        console.log(`AUDIT: D√âMARRAGE AUDIT COMPLET`);
        console.log(`AUDIT: Document ID recherch√©: ${documentId}`);
        console.log(`AUDIT: Type: ${typeof documentId}, Longueur: ${documentId.length}`);
        console.log(`AUDIT: Format UUID valide: ${/^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(documentId)}`);

        // AUDIT 0: V√©rifier l'authentification
        console.log("AUDIT: V√©rification de l'√©tat d'authentification...");
        const { data: { session }, error: sessionError } = await supabase.auth.getSession();
        console.log("AUDIT: Session actuelle:", {
          hasSession: !!session,
          userId: session?.user?.id,
          userEmail: session?.user?.email,
          sessionError
        });

        // AUDIT 1: Test de connexion Supabase basique
        console.log("AUDIT: Test de connexion Supabase...");
        const { data: testData, error: testError } = await supabase
          .from('pdf_documents')
          .select('count')
          .limit(1);
        
        console.log("AUDIT: Test connexion - Donn√©es:", testData);
        console.log("AUDIT: Test connexion - Erreur:", testError);

        if (abortController.signal.aborted) {
          console.log("AUDIT: Requ√™te annul√©e apr√®s test connexion");
          return;
        }
        
        // AUDIT 2: Recherche large dans pdf_documents AVEC d√©tails complets
        console.log("AUDIT: ====== RECHERCHE DANS PDF_DOCUMENTS ======");
        const { data: allPdfDocs, error: allPdfError, count: totalCount } = await supabase
          .from('pdf_documents')
          .select('*', { count: 'exact' })
          .limit(10);

        console.log('AUDIT: Recherche large - Nombre total:', totalCount);
        console.log('AUDIT: Recherche large - Donn√©es brutes:', allPdfDocs);
        console.log('AUDIT: Recherche large - Erreur d√©taill√©e:', allPdfError);

        if (allPdfDocs && allPdfDocs.length > 0) {
          console.log("AUDIT: Documents trouv√©s - comparaison des IDs:");
          allPdfDocs.forEach((doc, index) => {
            const idMatch = doc.id === documentId;
            console.log(`  üìÑ Doc ${index + 1}: ID="${doc.id}" | Nom="${doc.file_name}" | Match=${idMatch}`);
            if (idMatch) {
              console.log(`  ‚úÖ TROUV√â! Document correspondant:`, doc);
            }
          });
        } else {
          console.log("AUDIT: ‚ùå Aucun document trouv√© dans pdf_documents");
        }

        // AUDIT 3: Recherche sp√©cifique par ID dans pdf_documents
        console.log("AUDIT: Recherche sp√©cifique dans pdf_documents...");
        const { data: pdfData, error: pdfError } = await supabase
          .from('pdf_documents')
          .select('*')
          .eq('id', documentId);

        console.log('AUDIT: Recherche sp√©cifique - Donn√©es:', pdfData);
        console.log('AUDIT: Recherche sp√©cifique - Erreur:', pdfError);
        console.log('AUDIT: Recherche sp√©cifique - Type de donn√©es:', typeof pdfData);
        console.log('AUDIT: Recherche sp√©cifique - Est-ce un array?', Array.isArray(pdfData));
        console.log('AUDIT: Recherche sp√©cifique - Longueur:', pdfData?.length);

        if (abortController.signal.aborted) {
          console.log("AUDIT: Requ√™te annul√©e apr√®s recherche sp√©cifique");
          return;
        }

        // AUDIT 4: V√©rification des erreurs
        if (pdfError) {
          console.error("AUDIT: ‚ùå ERREUR dans pdf_documents:", {
            code: pdfError.code,
            message: pdfError.message,
            details: pdfError.details,
            hint: pdfError.hint
          });
          
          if (pdfError.code !== 'PGRST116') {
            throw pdfError;
          }
        }

        // AUDIT 5: Traitement des donn√©es trouv√©es
        if (pdfData && pdfData.length > 0) {
          console.log("AUDIT: ‚úÖ Document trouv√© dans pdf_documents:", pdfData[0]);
          const foundDoc = pdfData[0];
          const newDocument = {
            id: foundDoc.id,
            file_name: foundDoc.file_name,
            file_path: foundDoc.file_path,
            file_type: foundDoc.content_type || 'application/pdf',
            content_type: foundDoc.content_type,
            user_id: foundDoc.user_id,
            created_at: foundDoc.created_at,
            description: foundDoc.description,
            file_size: foundDoc.file_size,
            updated_at: foundDoc.updated_at,
            external_id: foundDoc.external_id
          };
          console.log("AUDIT: Document format√©:", newDocument);
          setDocument(newDocument);
          setLoading(false);
          loadingRef.current = false;
          console.log("AUDIT: ===========================================");
          return;
        }

        console.log("AUDIT: ‚ùå Document non trouv√© dans pdf_documents, recherche dans directives...");

        // Recherche dans directives
        const { data: directiveData, error: directiveError } = await supabase
          .from('directives')
          .select('*')
          .eq('id', documentId);

        console.log('AUDIT: Directives - Donn√©es:', directiveData);
        console.log('AUDIT: Directives - Erreur:', directiveError);

        if (abortController.signal.aborted) {
          return;
        }

        if (directiveError && directiveError.code !== 'PGRST116') {
          console.error("AUDIT: Erreur dans directives:", directiveError);
          throw directiveError;
        }

        if (directiveData && directiveData.length > 0) {
          console.log("AUDIT: ‚úÖ Document trouv√© dans directives");
          const content = directiveData[0].content as any;
          const newDocument = {
            id: directiveData[0].id,
            file_name: content?.title || content?.titre || 'Directive anticip√©e',
            file_path: `data:application/pdf;base64,${btoa('PDF directive')}`,
            file_type: 'application/json',
            content_type: 'application/json',
            user_id: directiveData[0].user_id,
            created_at: directiveData[0].created_at,
            description: 'Directive anticip√©e',
            content: directiveData[0].content
          };
          console.log("AUDIT: Document final (directive):", newDocument);
          setDocument(newDocument);
          setLoading(false);
          loadingRef.current = false;
          return;
        }

        console.log("AUDIT: ‚ùå Document non trouv√© dans directives, recherche dans shared_documents...");

        // Recherche dans shared_documents
        const { data: sharedData, error: sharedError } = await supabase
          .from('shared_documents')
          .select('*')
          .eq('document_id', documentId);

        console.log('AUDIT: Shared documents - Donn√©es:', sharedData);
        console.log('AUDIT: Shared documents - Erreur:', sharedError);

        if (abortController.signal.aborted) {
          return;
        }

        if (sharedError && sharedError.code !== 'PGRST116') {
          console.error("AUDIT: Erreur dans shared_documents:", sharedError);
          throw sharedError;
        }

        if (sharedData && sharedData.length > 0) {
          console.log("AUDIT: ‚úÖ Document trouv√© dans shared_documents");
          const documentData = sharedData[0].document_data as any;
          const newDocument = {
            id: sharedData[0].document_id,
            file_name: `Document partag√©`,
            file_path: documentData?.file_path || '#',
            file_type: sharedData[0].document_type || 'application/pdf',
            content_type: sharedData[0].document_type,
            user_id: sharedData[0].user_id,
            created_at: sharedData[0].shared_at,
            description: 'Document partag√©'
          };
          console.log("AUDIT: Document final (shared):", newDocument);
          setDocument(newDocument);
          setLoading(false);
          loadingRef.current = false;
          return;
        }

        // AUDIT FINAL: R√©sum√© complet
        console.log("AUDIT: ===========================================");
        console.log("AUDIT: ‚ùå √âCHEC COMPLET - R√âSUM√â:");
        console.log(`AUDIT: - Document ID: ${documentId}`);
        console.log(`AUDIT: - Session utilisateur: ${session ? 'OUI' : 'NON'}`);
        console.log(`AUDIT: - pdf_documents: ${pdfData?.length || 0} r√©sultats`);
        console.log(`AUDIT: - directives: ${directiveData?.length || 0} r√©sultats`);
        console.log(`AUDIT: - shared_documents: ${sharedData?.length || 0} r√©sultats`);
        console.log("AUDIT: ===========================================");
        
        const errorMsg = `Document avec l'ID ${documentId} introuvable dans toutes les tables. V√©rifiez que l'ID est correct et que vous avez les permissions d'acc√®s.`;
        console.error("AUDIT: ERREUR FINALE:", errorMsg);
        throw new Error(errorMsg);

      } catch (err: any) {
        if (abortController.signal.aborted) {
          console.log("AUDIT: Requ√™te annul√©e");
          return;
        }

        console.error(`AUDIT: ‚ùå ERREUR DANS LE PROCESSUS:`, {
          error: err,
          message: err.message,
          stack: err.stack,
          name: err.name
        });
        
        const errorMessage = err.message || 'Impossible de charger le document';
        console.error("AUDIT: Erreur finale:", errorMessage);
        setError(errorMessage);
        setLoading(false);
        loadingRef.current = false;
      }
    };

    loadDocument();

    // Cleanup function
    return () => {
      if (abortControllerRef.current) {
        abortControllerRef.current.abort();
      }
      loadingRef.current = false;
    };
  }, [documentId, retryCount]);

  // Fonction manuelle de retry
  const retryLoad = () => {
    if (!loadingRef.current) {
      console.log("AUDIT: Retry manuel d√©clench√©");
      setRetryCount(prev => prev + 1);
      setError(null);
      setDocument(null);
    }
  };

  return {
    document,
    loading,
    error,
    retryCount,
    retryLoad,
    setError
  };
};
